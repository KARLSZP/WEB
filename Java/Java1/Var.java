public class Var{
    public static void main(String args[]){
        float temperature;    // 定义一个浮点类型的变量
        temperature = 26;     // 把右边的值赋给左边的变量
        System.out.println("temperature = " + temperature);
        int x = 12;
        Integer y = 30; //y为对象
        long s = 40;  
        Long t = 50L;   //要用Long常量
        System.out.println("x=" + x + "; y=" + y);
        System.out.println("s=" + s + "; t=" + t);
    }
}



